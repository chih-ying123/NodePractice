CREATE OR REPLACE TABLE `betdata_aws` (
  `Rowid` BIGINT(20) UNSIGNED NOT NULL AUTO_INCREMENT,
  `username` VARCHAR(64) DEFAULT NULL,
  `orderId` INT(64) DEFAULT NULL,
  `orderid_Hash` BIGINT(20) DEFAULT NULL,    
  `stake` DECIMAL(18,2) DEFAULT NULL COMMENT '投注扣款额',
  `bet` DECIMAL(18,2) DEFAULT NULL COMMENT '投注',
  `win` DECIMAL(18,2) DEFAULT NULL COMMENT '赢奖',
  `balanceBefore` DECIMAL(18,2) DEFAULT NULL COMMENT '投注前余额',
  `balanceAfter` DECIMAL(18,2) DEFAULT NULL COMMENT '投注后余额',
  `operationType` VARCHAR(32) DEFAULT NULL,
  `operationId` INT(5) DEFAULT NULL,
  `betTime` DATETIME DEFAULT NULL,
  `cnGameName` VARCHAR(64) DEFAULT NULL,
  `enGameName` VARCHAR(64) DEFAULT NULL,
  `device` INT(5) DEFAULT NULL COMMENT '设0: 行动装置 1: 网页',
  `gameId` VARCHAR(32) DEFAULT NULL,
  `finalView` TEXT DEFAULT NULL,
  `txnId` VARCHAR(128) DEFAULT NULL,
  `currency` VARCHAR(32) DEFAULT NULL,
  PRIMARY KEY (`Rowid`)
) ENGINE=INNODB DEFAULT CHARSET=utf8mb4